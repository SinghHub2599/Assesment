package com.ATDevServices.Student2;

public class Student {
private Integer sid;
private String sname;
private String sbrance;
public Integer getSid() {
	return sid;
}
public void setSid(Integer sid) {
	this.sid = sid;
}
public String getSname() {
	return sname;
}
public void setSname(String sname) {
	this.sname = sname;
}

public String getSbrance() {
	return sbrance;
}
public void setSbrance(String sbrance) {
	this.sbrance = sbrance;
}
public Long getSmob() {
	return smob;
}
public void setSmob(Long smob) {
	this.smob = smob;
}
public String getAddress() {
	return Address;
}
public void setAddress(String address) {
	Address = address;
}
private Long smob;
private String Address;

}
